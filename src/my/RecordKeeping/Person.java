public class Person {
    public String firstName;
    public String surname;
    public String email;
    public String PPSN;
    public String DOB;
    public String addressL1;
    public String addressL2;
    public String county;
    public String eircode;
    public int phoneNumber;

    /**
     * @return the firstName
     */
    public String getFirstName() {
        return firstName;
    }

    /**
     * @param firstName the firstName to set
     */
    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    /**
     * @return the surname
     */
    public String getSurname() {
        return surname;
    }

    /**
     * @param surname the surname to set
     */
    public void setSurname(String surname) {
        this.surname = surname;
    }

    /**
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * @param email the email to set
     */
    public void setEmail(String email) {
        this.email = email;
    }

    /**
     * @return the PPSN
     */
    public String getPPSN() {
        return PPSN;
    }

    /**
     * @param PPSN the PPSN to set
     */
    public void setPPSN(String PPSN) {
        this.PPSN = PPSN;
    }

    /**
     * @return the DOB
     */
    public String getDOB() {
        return DOB;
    }

    /**
     * @param DOB the DOB to set
     */
    public void setDOB(String DOB) {
        this.DOB = DOB;
    }

    /**
     * @return the addressL1
     */
    public String getAddressL1() {
        return addressL1;
    }

    /**
     * @param addressL1 the addressL1 to set
     */
    public void setAddressL1(String addressL1) {
        this.addressL1 = addressL1;
    }

    /**
     * @return the addressL2
     */
    public String getAddressL2() {
        return addressL2;
    }

    /**
     * @param addressL2 the addressL2 to set
     */
    public void setAddressL2(String addressL2) {
        this.addressL2 = addressL2;
    }

    /**
     * @return the county
     */
    public String getCounty() {
        return county;
    }

    /**
     * @param county the county to set
     */
    public void setCounty(String county) {
        this.county = county;
    }

    /**
     * @return the eircode
     */
    public String getEircode() {
        return eircode;
    }

    /**
     * @param eircode the eircode to set
     */
    public void setEircode(String eircode) {
        this.eircode = eircode;
    }

    /**
     * @return the phoneNumber
     */
    public int getPhoneNumber() {
        return phoneNumber;
    }

    /**
     * @param phoneNumber the phoneNumber to set
     */
    public void setPhoneNumber(int phoneNumber) {
        this.phoneNumber = phoneNumber;
    }
}
